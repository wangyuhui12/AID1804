
# 　１、用字符串*运算符打印三角形
#     要求输入一个整数，此整数代表此三角形离左侧的字节数
#     如：
#         $ python3 tri_angle.py
#         请输入离左侧的距离：３
#         则打印
#         　　*
#          ***
#         *****
#        *******

n = int(input("请输入一个整数："))

print(' '*n,"   *   ")
print(' '*n,"  ***  ")
print(' '*n," ***** ")
print(' '*n,"*******")